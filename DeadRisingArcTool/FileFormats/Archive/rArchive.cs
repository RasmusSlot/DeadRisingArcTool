        public static uint ComputeHashcode(string str)
        {
            uint hashcode = 0;

            // Loop for the length of the string.
            for (int i = 0; i < str.Length; i++)
            {
                // Compute the hashcode table index.
                int nextChar = i < str.Length - 1 ? (int)char.ToLower(str[i + 1]) : 0;
                int index = (((int)char.ToLower(str[i]) << 6) - 2048) | (nextChar - 32);

