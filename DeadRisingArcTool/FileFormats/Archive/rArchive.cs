        public override byte[] ToBuffer()
        {
            // Create a new memory stream to back our file with.
            MemoryStream ms = new MemoryStream();
            EndianWriter writer = new EndianWriter(this.IsBigEndian == true ? Endianness.Big : Endianness.Little, ms);

            // Update the number of files in the index.
            this.header.NumberOfFiles = (short)this.FileIds.Length;

            // Write the new archive index header.
            writer.Write(this.header.Magic);
            writer.Write(this.header.Version);
            writer.Write(this.header.NumberOfFiles);

            // Loop and write the list of file ids.
            for (int i = 0; i < this.FileIds.Length; i++)
            {
                // Write the file id.
                writer.Write(this.FileIds[i]);
            }

            // Close the binary writer and return the memory stream as a byte array.
            writer.Close();
            return ms.ToArray();
        }

        public static rArchive FromGameResource(byte[] buffer, string fileName, DatumIndex datum, ResourceType fileType, bool isBigEndian)
        {
            // Make sure the buffer is large enough to hold the header.
            if (buffer.Length < ArchiveHeader.kSizeOf)
                return null;

            // Create a new rArchive to read into.
            rArchive archive = new rArchive(fileName, datum, fileType, isBigEndian);

            // Create a new memory stream and binary reader for the buffer.
            MemoryStream ms = new MemoryStream(buffer);
            EndianReader reader = new EndianReader(isBigEndian == true ? Endianness.Big : Endianness.Little, ms);

            // Read the archive header.
            archive.header.Magic = reader.ReadInt32();
            archive.header.Version = reader.ReadInt16();
            archive.header.NumberOfFiles = reader.ReadInt16();

            // Make sure the header is valid.
            if (archive.header.Magic != ArchiveHeader.kHeaderMagic2 || archive.header.Version != ArchiveHeader.kVersion)
            {
                // Header magic or version are invalid.
                return null;
            }

            // Allocate and read the file ids.
            archive.FileIds = new ulong[archive.header.NumberOfFiles];
            for (int i = 0; i < archive.header.NumberOfFiles; i++)
            {
                // Read the file id.
                archive.FileIds[i] = reader.ReadUInt64();
            }

            // Close the binary reader and memory stream.
            reader.Close();
            ms.Close();

            // Return the archive object.
            return archive;
        }

